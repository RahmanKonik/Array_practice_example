package Array_practice_example;

public class MultiDimArrayDemo {

    public static void main(String[] args) {

        String first_name[][] = {{"Mr", "Mrs", "Miss"},{
            "Jomes", "Jemi"
        }};


        System.out.println(first_name[0][0] +" "+first_name[1][0]);
        System.out.println(first_name[0][1] +" "+first_name[1][1]);
        
    }
    
}
